					<span class="focus">Who Are We?</span>
					<p>The Association for Computing Machinery  (ACM) is an organization which deals with  issues related to computers, Computer  Science, and Computer Engineering. This  includes applications of computer technology and new developments in the  field. In addition, the Duke chapter of  the ACM is the official computer science  majors union for Duke University.</p>
					
