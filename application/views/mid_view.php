					<span class="focus">What Do We Do?</span>
					<p>The Duke chapter of the ACM is active in  many areas and serves a variety of  interests. The following lists the  activities of the Duke chapter of the  ACM:</p>
					<ul>
						<li>Speakers on the latest topics in the world of computing</li>
						<li>Discussions concerning the options for majors in Computer Science</li>
						<li>Information concerning job and internship opportunities in the field of computers</li>
						<li>ACM Regional programming contest</li>
					</ul>
					
					
				